$("#_list").height($("#ClubIntro").outerHeight());
$(".Club").click(function(){
    $('#current').removeAttr("id");
    $(this).attr("id","current");
    $("#_list").height($("#ClubIntro").outerHeight());
    $("#ClubIntro").css("display","block");
    $("#LABIntro").css("display","none");
    $("#CenterIntro").css("display","none");
    $(document).attr("title","俱乐部简介");
});
